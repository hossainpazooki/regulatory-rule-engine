//! Generate the synthetic golden fixtures under `fixtures/artifacts/`.
//!
//! Gate 1 runs in **synthetic mode**: the example artifacts come from
//! [`ke_core::examples`], not from the platform pipeline. The platform-driven
//! cross-corpus path is deferred until the recorded `fixtures/rules/SOURCE.md`
//! SHA is reconciled with the platform `HEAD`; `scripts/generate-golden-fixtures.sh`
//! holds the SHA guard and prints that notice.
//!
//! Each artifact `<id>` gets a triple:
//! - `canonical.bin`  — the authoritative canonical bytes;
//! - `source.json`    — the decoded value, pretty-printed (review only);
//! - `manifest.json`  — a synthetic manifest (review only).
//!
//! A `fixtures/artifacts/MANIFEST.md` provenance ledger lists every id with its
//! version triplet and BLAKE3. The generator is deterministic and idempotent —
//! re-running it produces byte-identical output.

use ke_core::canonical::{decode_policy, decode_rule, encode_policy, encode_rule};
use ke_core::examples;
use ke_core::ir::JurisdictionDate;
use ke_core::manifest::{ArtifactKind, Manifest};
use ke_core::version::{CANONICALIZATION_VERSION, CODEC_VERSION, IR_SCHEMA_VERSION};
use std::fs;
use std::path::{Path, PathBuf};

fn repo_root() -> PathBuf {
    // crates/ke-core -> repo root
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(Path::parent)
        .expect("crate is two levels below repo root")
        .to_path_buf()
}

struct Row {
    id: String,
    kind: &'static str,
    blake3: String,
}

fn write_triple(
    artifacts_dir: &Path,
    id: &str,
    kind: &'static str,
    canonical: &[u8],
    source_json: String,
    manifest: &Manifest,
    rows: &mut Vec<Row>,
) -> std::io::Result<()> {
    let dir = artifacts_dir.join(id);
    fs::create_dir_all(&dir)?;
    fs::write(dir.join("canonical.bin"), canonical)?;
    fs::write(dir.join("source.json"), source_json)?;
    let mut manifest_json = serde_json::to_string_pretty(manifest).expect("manifest serializes");
    manifest_json.push('\n');
    fs::write(dir.join("manifest.json"), manifest_json)?;
    rows.push(Row {
        id: id.to_string(),
        kind,
        blake3: blake3::hash(canonical).to_hex().to_string(),
    });
    Ok(())
}

fn pretty<T: serde::Serialize>(value: &T) -> String {
    let mut s = serde_json::to_string_pretty(value).expect("value serializes");
    s.push('\n');
    s
}

fn main() -> std::io::Result<()> {
    let artifacts_dir = repo_root().join("fixtures").join("artifacts");
    fs::create_dir_all(&artifacts_dir)?;
    let mut rows: Vec<Row> = Vec::new();

    // Rule artifacts (RegimePack kind).
    for (id, rule) in examples::rules() {
        let canonical = encode_rule(&rule).expect("example rule encodes");
        let decoded = decode_rule(&canonical).expect("example rule round-trips");
        let effective_from = rule
            .effective_window
            .as_ref()
            .map(|w| w.effective_from)
            .unwrap_or_else(|| JurisdictionDate::new(1900, 1, 1));
        let manifest = examples::synthetic_manifest(
            ArtifactKind::RegimePack,
            "mica_2023",
            effective_from,
            &canonical,
        );
        write_triple(
            &artifacts_dir,
            &id,
            "RegimePack",
            &canonical,
            pretty(&decoded),
            &manifest,
            &mut rows,
        )?;
    }

    // Policy artifact (PolicyBundle kind).
    {
        let (id, bundle) = examples::policy();
        let canonical = encode_policy(&bundle).expect("example policy encodes");
        let decoded = decode_policy(&canonical).expect("example policy round-trips");
        let manifest = examples::synthetic_manifest(
            ArtifactKind::PolicyBundle,
            "policy",
            bundle.effective_window.effective_from,
            &canonical,
        );
        write_triple(
            &artifacts_dir,
            &id,
            "PolicyBundle",
            &canonical,
            pretty(&decoded),
            &manifest,
            &mut rows,
        )?;
    }

    // Provenance ledger (deterministic order).
    rows.sort_by(|a, b| a.id.cmp(&b.id));
    let mut ledger = String::new();
    ledger.push_str("# fixtures/artifacts/ — golden provenance ledger\n\n");
    ledger.push_str(
        "Generated by `cargo run -p ke-core --bin gen-fixtures` (synthetic mode).\n\
         Do not hand-edit; re-run the generator. `canonical.bin` is authoritative;\n\
         `source.json` and `manifest.json` are regenerated review views.\n\n",
    );
    ledger.push_str(&format!(
        "- ir_schema_version: {IR_SCHEMA_VERSION}\n- codec_version: {CODEC_VERSION}\n\
         - canonicalization_version: {CANONICALIZATION_VERSION}\n\
         - source: synthetic (platform-driven cross-corpus bytes deferred; see \
         `fixtures/rules/SOURCE.md`)\n\n",
    ));
    ledger.push_str("| artifact_id | kind | blake3(canonical.bin) |\n");
    ledger.push_str("| ----------- | ---- | --------------------- |\n");
    for r in &rows {
        ledger.push_str(&format!("| `{}` | {} | `{}` |\n", r.id, r.kind, r.blake3));
    }
    fs::write(artifacts_dir.join("MANIFEST.md"), ledger)?;

    eprintln!(
        "wrote {} artifact(s) to {}",
        rows.len(),
        artifacts_dir.display()
    );
    Ok(())
}
